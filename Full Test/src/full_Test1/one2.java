    public static String backspace(String s){
        
        
        String str="";
        int size= s.length();
        int count=0;
        for(int i=size-1;i>=0;i--)
        {
           if(s.charAt(i) == '#')
               count++;
            else{
                if(count==0)
             str = s.charAt(i) + str;
                
                else
                   count--;
        }
       
        }
        return str;
      }

